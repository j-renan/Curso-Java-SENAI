    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomeProduto = jTextFieldProduto.getText();
        Float valorProduto = Float.parseFloat(jTextFieldPreco.getText());
        String txtPercentualDesconto = (String) jComboBoxDesconto.getSelectedItem();
        Float porcentagemDesconto = Float.parseFloat(txtPercentualDesconto.split("%")[0]) / 100;
        
        float valorDoDesconto = valorProduto * porcentagemDesconto;
        float produtoComDesconto = valorProduto - valorDoDesconto;
        
        String mensagem = String.format("Valor original do produto R$ %.2f \n" +
                "Valor do desconto R$ %.2f\n" +
                "Valor do produto com desconto R$ %.2f", valorProduto, valorDoDesconto,produtoComDesconto );
        
        jTextAreaSaida.setText(mensagem);
    }//GEN-LAST:event_jButton1ActionPerformed

