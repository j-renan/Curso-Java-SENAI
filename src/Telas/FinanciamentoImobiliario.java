    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        String cliente = jTextFieldCliente.getText();
        float valorImovel = Float.parseFloat(jTextFieldValorImovel.getText());
        float taxaJuros = Float.parseFloat(jTextFieldTaxaJuros.getText()) / 100;
        byte qtdAnos = Byte.parseByte((String) jComboBoxParcelamento.getSelectedItem());
        float rendaMensal = Float.parseFloat(jTextFieldRendaMensal.getText());
        
        short qtdMeses = (short) (qtdAnos * 12);
        float valorJurosMensal = (valorImovel / qtdMeses) * taxaJuros;
        float totalJuros = valorJurosMensal * qtdMeses;       
        float totalImovelComJuros = totalJuros + valorImovel;
        float valorMensalidade = totalImovelComJuros / qtdMeses;
        float margemParaAprovacao = rendaMensal * 0.3f;
        
        String mensagem = "";
        
        if (margemParaAprovacao >= valorMensalidade) {
            mensagem = "Segue o relat칩rio do financimanto"
                + " do cliente " + cliente + "\n"
                + "Valor do im칩vel: R$" + valorImovel + "\n"
                + "Qtd anos: " + qtdAnos + "\n"
                + "Qrd meses: " + qtdMeses + "\n"
                + "Valor do juros mensal: " + valorJurosMensal + "\n"
                + "Total de juros do financiamento: " + totalJuros + "\n"
                + "Mensalidade: " + valorMensalidade + "\n"
                + "Total do imovel com juros: " + totalImovelComJuros;            
        } else {
            mensagem = "Infelizmente seu financiamento n칚o foi aprovado. \n"
                    + "A mensalidade supera os 30% da sua renda mensal. \n"
                    + "Sua margem para aprova칞칚o: " + margemParaAprovacao + "\n"
                    + "Valor da mensalidade: " + valorMensalidade;
        }       
        
        jTextArea1.setText(mensagem);
    }//GEN-LAST:event_jButtonCalcularActionPerformed

